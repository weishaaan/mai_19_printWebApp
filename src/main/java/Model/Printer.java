package Model;

public class Printer {
    int dpi;
    String ip;
    String name;
    String type;
    Boolean status;

    public Printer(int dpi, String ip, String name, String type, Boolean status) {
        this.dpi = dpi;
        this.ip = ip;
        this.name = name;
        this.type = type;
        this.status = status;
    }
    
    
    
}
